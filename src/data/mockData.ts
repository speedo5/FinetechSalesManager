// Helper to generate unique FO code
export const generateFOCode = (users: User[]): string => {
  const foUsers = users.filter(u => u.role === 'field_officer' && u.foCode);
  const maxNum = foUsers.reduce((max, u) => {
    const num = parseInt(u.foCode?.replace('FO-', '') || '0');
    return num > max ? num : max;
  }, 0);
  return `FO-${String(maxNum + 1).padStart(3, '0')}`;
};
